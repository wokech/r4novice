# Variable assignment

# Using the "<-" assignment operator

variable_1 <- 5
variable_1 

# Using the "=" assignment operator

variable_2 <- 10
variable_2 

# The order of the value and the variable can be switched 
# with the "->" assignment operator

15 -> variable_3
variable_3

# Two variables can be assigned to one value

variable_4 <- variable_5 <- 30
variable_4
variable_5
